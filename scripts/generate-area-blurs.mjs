#!/usr/bin/env node
/**
 * Generate tiny base64 blur placeholders for every area hero image.
 *
 * For every JPG in public/areas/, this script writes an 8×4 px JPEG
 * (≈ 250-400 bytes), encodes it as a data URI, and emits a TypeScript
 * module that the card components import. CSS scales the placeholder
 * to fill the image frame; the natural bilinear interpolation gives a
 * soft blur while the full image streams. Once the real image lands,
 * a 280ms fade brings it to focus.
 *
 * Same approach Next.js's `placeholder="blur"` uses on the landing
 * hero — we apply it manually to the area cards because they use
 * <picture> with AVIF + JPG sources.
 *
 * Usage:
 *   node scripts/generate-area-blurs.mjs
 *
 * Output:
 *   src/data/area-blurs.ts
 */

import sharp from 'sharp';
import { readdir, writeFile, mkdir } from 'node:fs/promises';
import path from 'node:path';

const SRC_DIR = path.resolve('public/areas');
const OUT_FILE = path.resolve('src/data/area-blurs.ts');

async function main() {
  const files = await readdir(SRC_DIR);
  const heroes = files.filter((f) => f.endsWith('.jpg'));

  console.log(`Generating blurs for ${heroes.length} area heroes…`);

  const blurs = {};
  for (const file of heroes) {
    const slug = file.replace(/\.jpg$/, '');
    const buf = await sharp(path.join(SRC_DIR, file))
      .resize(8, 4, { fit: 'inside' })
      .jpeg({ quality: 50, mozjpeg: true })
      .toBuffer();
    blurs[slug] = `data:image/jpeg;base64,${buf.toString('base64')}`;
  }

  await mkdir(path.dirname(OUT_FILE), { recursive: true });
  await writeFile(
    OUT_FILE,
    `// Auto-generated by scripts/generate-area-blurs.mjs\n` +
      `// Run \`node scripts/generate-area-blurs.mjs\` after replacing any hero image.\n\n` +
      `export const AREA_BLURS: Record<string, string> = ${JSON.stringify(blurs, null, 2)};\n\n` +
      `export function blurFor(slug: string | null | undefined): string | undefined {\n` +
      `  if (!slug) return undefined;\n` +
      `  return AREA_BLURS[slug];\n` +
      `}\n`
  );

  const totalBytes = Object.values(blurs).reduce((n, s) => n + s.length, 0);
  console.log(`✔ wrote ${OUT_FILE}`);
  console.log(`  ${heroes.length} blurs, ~${(totalBytes / 1024).toFixed(1)} KB total`);
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
